cust_cntt_fpinfo.drop(['Unnamed: 0'], axis=1, inplace = True)

cust_cntt_fpinfo.info()

# 정제된 CLAIM_DATA 파일 불러오기
claim = pd.read_csv('ProcessedCLAIMData.csv', sep = ',', encoding = 'UTF-8')
claim

claim.info()

