import pandas as pd
import warnings
warnings.filterwarnings(action='ignore')

# CNTT_DATA, CUST_DATA, FPINFO_DATA 병합 파일 불러오기
cust_cntt_fpinfo = pd.read_csv('CNTT_FPINFO_CUST.csv', sep = ',', encoding = 'UTF-8')
